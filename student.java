public class student {
    protected int bDay;
    protected int bMonth;
    protected int bYear;

    public void setbDay(int bDay) {
        this.bDay = bDay;
    }

    public void setbMonth(int bMonth) {
        this.bMonth = bMonth;
    }

    public void setbYear(int bYear) {
        this.bYear = bYear;
    }

    public int getbDay() {
        return bDay;
    }

    public int getbMonth() {
        return bMonth;
    }

    public int getbYear() {
        return bYear;
    }

}
